package com.edu_manger_sys_entity;

public class ExampleInfo {
	private int exam_id;//考试id
	private String exam_time;//考试时间
	private String exam_place;//考试地点
	private int exam_type;//考试类型
	private int stu_id;//学生id
	private String sub_name;//科目名称
	
	
	
	/**
	 * 
	 * @ get，set方法
	 * @author hsy
	 * @return
	 */
	public int getExam_id() {
		return exam_id;
	}
	public void setExam_id(int examId) {
		exam_id = examId;
	}
	public String getExam_time() {
		return exam_time;
	}
	public void setExam_time(String examTime) {
		exam_time = examTime;
	}
	public String getExam_place() {
		return exam_place;
	}
	public void setExam_place(String examPlace) {
		exam_place = examPlace;
	}
	public int getExam_type() {
		return exam_type;
	}
	public void setExam_type(int examType) {
		exam_type = examType;
	}
	public int getStu_id() {
		return stu_id;
	}
	public void setStu_id(int stuId) {
		stu_id = stuId;
	}
	public String getSub_name() {
		return sub_name;
	}
	public void setSub_name(String subName) {
		sub_name = subName;
	}
	
	
	
	
	
	
}
