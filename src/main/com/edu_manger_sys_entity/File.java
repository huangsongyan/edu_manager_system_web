package com.edu_manger_sys_entity;

public class File {
	private int file_id;
	private String file_name;
	private String file_url;
	private String file_date;
	private int stu_id;
	private int tec_id;
	private int file_uploader;
	public int getFile_id() {
		return file_id;
	}
	public void setFile_id(int fileId) {
		file_id = fileId;
	}
	public String getFile_name() {
		return file_name;
	}
	public void setFile_name(String fileName) {
		file_name = fileName;
	}
	public String getFile_url() {
		return file_url;
	}
	public void setFile_url(String fileUrl) {
		file_url = fileUrl;
	}
	public String getFile_date() {
		return file_date;
	}
	public void setFile_date(String fileDate) {
		file_date = fileDate;
	}
	public int getStu_id() {
		return stu_id;
	}
	public void setStu_id(int stuId) {
		stu_id = stuId;
	}
	public int getFile_uploader() {
		return file_uploader;
	}
	public void setFile_uploader(int fileUploader) {
		file_uploader = fileUploader;
	}
	public int getTec_id() {
		return tec_id;
	}
	public void setTec_id(int tecId) {
		tec_id = tecId;
	}
	
	
	
	
	
}
